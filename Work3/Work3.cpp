#include <iostream>
using namespace std;
int main()
{
    double n1;
    cout << "Enter the first number: ";
    cin >> n1;

    double n2;
    cout << "Enter the second number: ";
    cin >> n2;

    double n3;
    cout << "Enter the third number: ";
    cin >> n3;

    cout << "\n" << "The sum of three numbers: " << n1 + n2 + n3 << "\n";
    cout << "The product of numbers: " << n1 * n2 * n3 << "\n";
}